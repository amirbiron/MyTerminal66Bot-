import time

def narrate(text):
    print(f"\n📖 {text}")
    time.sleep(2)

narrate("מומין אהב ליצור דברים. יום אחד הוא צייר את הציור הכי יפה שצייר אי פעם.")
narrate("הוא רץ להראות אותו לדניאל פוגל, שישב על הגשר עם המפוחית.")
narrate("דניאל פוגל הביט בציור ואמר: 'זה יפה, מומין' – וחזר לנגן.")
narrate("לבו של מומין צבט. הוא הלך משם לאט, קצת מאוכזב.")
narrate("למחרת בנה מומין מגדל מאבנים. הוא שוב רץ לקרוא לדניאל.")
narrate("דניאל ישב ליד מדורה, הרים יד לשלום – אבל נשאר במקומו.")
narrate("מומין התיישב עצוב על החול... וחיבק את ברכיו.")
narrate("אחרי זמן מה, דניאל הגיע: 'זה היה מגדל ממש יפה.'")
narrate("'סליחה שלא באתי מיד, הייתי צריך לחשוב על מנגינה חדשה.'")
narrate("מומין חייך. הוא הבין שדניאל עדיין חברו הטוב. ולפעמים – זה בסדר לקחת רגע לעצמנו.")
